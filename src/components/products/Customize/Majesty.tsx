// Convert RGB to HEX
const rgbToHex = (r, g, b) =>
    "#" +
    [r, g, b]
        .map((x) => {
            const hex = x.toString(16);
            return hex.length === 1 ? "0" + hex : hex;
        })
        .join("")
        .toUpperCase();

// Group by prefix (AA, AB, AC, AD, etc.)
const groupColors = (data) => {
    const groups = {};
    data.forEach((c) => {
        const [prefix] = c.name.split(" ");
        if (!groups[prefix]) groups[prefix] = [];
        groups[prefix].push(c);
    });
    return groups;
};

